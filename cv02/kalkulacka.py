a = float(input("Zadej číslo a: "))
b = float(input("Zadej číslo b: "))

soucet = a + b
rozdil = a - b
soucin = a * b
podil = a / b
modulo = a % b
expon = a ** b

print(a, "+", b, "= ", soucet)
print(a, "-", b, "= ", rozdil)
print(a, "*", b, "= ", soucin)
print(a, "/", b, "= ", podil)
print(a, "%", b, "= ", modulo)
print(a, "^", b, "= ", expon)
